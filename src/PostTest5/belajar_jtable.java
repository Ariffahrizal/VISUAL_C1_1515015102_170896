    private void hapusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_hapusMouseClicked
        // TODO add your handling code here:
        int pil = JOptionPane.showConfirmDialog(this, "Anda yakin ingin hapus data ini","Konfirmasi",JOptionPane.YES_NO_OPTION);
        if (pil == 0) {
            int baris = isitabel.getSelectedRow();
            model.removeRow(baris);
            JOptionPane.showMessageDialog(hapus, "Data berhasil dihapus");
            update.setEnabled(false);
            hapus.setEnabled(false);
        }
    }//GEN-LAST:event_hapusMouseClicked

