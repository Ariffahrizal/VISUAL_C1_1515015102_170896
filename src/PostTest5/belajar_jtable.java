    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        if(nama.getText().length()==0 || notelp.getText().length()==0 || alamat.getText().length()==0){
        JOptionPane.showMessageDialog(null, "Tidak boleh ada data yang kosong","Peringatan",JOptionPane.INFORMATION_MESSAGE);
        nama.requestFocus();
        }
        else{
        Object[] data = new Object [4];
        data[0] = nama.getText();
        data[1] = notelp.getText();
        data[2] = alamat.getText();
        data[3] = kota.getSelectedItem();
        model.addRow(data);
        
        nama.setEnabled(false);
        notelp.setEnabled(false);
        alamat.setEnabled(false);
        kota.setEnabled(false);
        simpan.setEnabled(false);
        tambah.setEnabled(true);
        }
    }//GEN-LAST:event_simpanActionPerformed

