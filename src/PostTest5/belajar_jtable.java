    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        int baris = isitabel.getSelectedRow();
        
        nama.setEnabled(true);
        notelp.setEnabled(true);
        alamat.setEnabled(true);
        kota.setEnabled(true);
        hapus.setEnabled(false);
        simpan.setEnabled(false);
        tambah.setEnabled(true);
        
        if(nama.getText().length()==0 || notelp.getText().length()==0 || alamat.getText().length()==0){
        JOptionPane.showMessageDialog(null, "Tidak boleh ada data yang kosong","Peringatan",JOptionPane.INFORMATION_MESSAGE);
        nama.requestFocus();
        }
        
        else{
        isitabel.setValueAt(nama.getText(), baris, 0);
        isitabel.setValueAt(notelp.getText(), baris, 1);
        isitabel.setValueAt(alamat.getText(), baris, 2);
        isitabel.setValueAt(kota.getSelectedItem(), baris, 3);
        }
    }//GEN-LAST:event_updateActionPerformed

